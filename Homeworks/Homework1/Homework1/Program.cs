using System;

class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя ввод числа
        Console.Write("Введите число: ");
        int n;

        // Проверяем, что введено корректное число
        // Цикл продолжается, пока не будет введено целое число больше 1
        while (!int.TryParse(Console.ReadLine(), out n) || n < 2)
        {
            Console.Write("Введите целое число больше 1: ");
        }

        // Сообщаем пользователю, что будем выводить простые числа
        Console.WriteLine($"Простые числа, не превышающие {n}:");

        // Цикл от 2 до n для проверки простых чисел
        for (int i = 2; i <= n; i++)
        {
            // Проверяем, является ли текущее число простым
            if (IsPrime(i))
            {
                // Если простое, выводим его на экран
                Console.Write(i + " ");
            }
        }
    }

    // Метод для проверки, является ли число простым
    static bool IsPrime(int number)
    {
        // Числа меньше или равные 1 не являются простыми
        if (number <= 1) return false;

        // Проверяем делимость числа от 2 до квадратного корня из number
        for (int i = 2; i <= Math.Sqrt(number); i++)
        {
            // Если число делится на i без остатка, оно не простое
            if (number % i == 0) return false;
        }

        // Если ни одно из условий не сработало, число простое
        return true;
    }
}
